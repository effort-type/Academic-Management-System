    private boolean InfoCompare(int index) {
        // 변경하고자 하는 값이 일치할 때 true 반환
        if ((temp_name.getText().equals(name_list.get(index))) && (temp_major.getText().equals(major_list.get(index))) && (temp_num.getText().equals(num_list.get(index)))) {
            return true;
        } else {
            return false;
        }
    }

    // 동명이인 해결 완료, 고민 : 학번은 변경되는 일이 없으니까 변경요소에 넣지말까?
    private void NewFileCreat() {
        try {

            File file = new File(URL);
            FileWriter writer;
            writer = new FileWriter(file, false);
            for (int i = 0; i < name_list.size(); i++) {
                String s = "/";
                String n = "\n";
                writer.write(num_list.get(i));
                writer.write(s);
                writer.write(jumin_list.get(i));
                writer.write(s);
                writer.write(major_list.get(i));
                writer.write(s);
                writer.write(name_list.get(i));
                writer.write(n);

                writer.flush();// 출력은 버퍼에 쌓여있기에 쌓인 버퍼를 목적지로 보내줌
            }
            JOptionPane.showMessageDialog(null, "정보 수정 완료");
            writer.close();

            jumin_list.clear();
            num_list.clear();
            major_list.clear();
            name_list.clear();

        } catch (IOException ex) {
            System.out.println("오류발생");
        }
    }

